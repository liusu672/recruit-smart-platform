function aiMatch(score, summary, risk) {
  return {
    matchScore: score,
    recommendLevel: score >= 80 ? 'HIGH' : score >= 60 ? 'MEDIUM' : 'LOW',
    recommendReason: summary,
    highlightSummary: '相关项目经历与目标岗位方向一致',
    riskSummary: risk,
    modelName: 'demo-model',
    generatedAt: '2026-07-15T09:00:00',
  }
}

function timeline(id, title, description, actorName, occurredAt, source = 'BUSINESS') {
  return { id, title, description, actorName, occurredAt, source, relatedObject: `投递 #${id}` }
}

export function getPipelineStatusText(status) {
  const labels = {
    SUBMITTED: '已投递',
    SCREENING: '筛选中',
    SCREEN_PASSED: '初筛通过',
    SCREEN_REJECT: '初筛未通过',
    INTERVIEWING: '面试中',
    INTERVIEW_REJECT: '面试未通过',
    OFFERED: 'Offer 阶段',
    HIRED: '已入职',
    OFFER_REJECT: 'Offer 拒绝',
    WITHDRAWN: '已撤回',
  }
  return labels[status] ?? status
}

