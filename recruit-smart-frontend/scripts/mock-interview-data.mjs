function scorecard(scores = [], evidence = []) {
  const definitions = [
    ['professional', '专业能力', '是否具备岗位要求的核心知识、方法与实践深度。'],
    ['problem-solving', '问题解决', '能否拆解问题、说明判断依据并形成可执行方案。'],
    ['collaboration', '协作与影响力', '能否清晰沟通、推动协作并对结果负责。'],
    ['reflection', '结果与复盘', '能否量化结果、识别不足并沉淀后续改进。'],
  ]
  return definitions.map(([key, label, description], index) => ({
    key,
    label,
    description,
    score: scores[index] ?? null,
    evidence: evidence[index] ?? '',
  }))
}

